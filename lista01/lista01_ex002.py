#2-Construa um programa que armazena em duas variaveis duas notas e apresenta a média entre as duas

nota1 = int(input("Insira a nota 1: "))
nota2 = int(input("Insira a nota 2: "))

media = (nota1+nota2)/2

print("Média: " + str(media))

